            // Function to animate odometer from 0 to data-count
            function animateOdometer() {
                odometers.forEach(odometer => {
                    const count = odometer.getAttribute('data-count');

                    // Create the odometer instance
                    odometer.innerHTML = '0'; // Ensure it starts at 0

                    // Animate the count using GSAP
                    gsap.to({}, {
                        duration: 2, // Duration of the animation in seconds
                        onUpdate: function() {
                            const progress = this.progress(); // Get the progress of the animation
                            const currentCount = Math.floor(progress * count); // Calculate current count
                            odometer.innerHTML = currentCount; // Update odometer value
                        },
                        onComplete: function() {
                            odometer.innerHTML = count; // Ensure it ends with the correct value
                        }
                    });
                });
            }

            // Trigger animation
            animateOdometer();
        });
    </script>
